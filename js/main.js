window.onscroll = function () {
  scrollFunction();
};

function scrollFunction() {
  if (document.body.scrollTop > 80 || document.documentElement.scrollTop > 80) {
    document.getElementById("header-img").style.width = "175px";
  } else {
    document.getElementById("header-img").style.width = "250px";
  }
}

